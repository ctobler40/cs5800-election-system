drop database if exists election_system;
create database election_system;
use election_system;

create table role (
	id int primary key auto_increment,
	roleName varchar(30) not null
);

create table race (
	id int primary key auto_increment,
    raceName varchar(50) not null,
    raceType int not null,   -- 1 is Presidential, 2 is Government, 3 is House
    electionDate date not null,
    isActive bool not null
);

create table state (
	id int primary key auto_increment,
    stateName varChar(20) not null,
    abbr varChar(2) not null,
    population float not null,
    electoralVotes int not null,
    voteDay Date not null
);

create table county (
	id int primary key auto_increment,
    countyName varChar(25) not null,
    state_id int not null,
	constraint fk_county_state_id
		foreign key (state_id)
		references state(id)
);

create table candidate (
	id int primary key auto_increment,
	firstName varchar(50) not null,
	lastName varchar(50) not null,
    party varchar(1) null,
    race_id int not null,
    state_running int null,
    county_running int null,
    information longtext not null,
	constraint fk_candidate_race_id
		foreign key (state_running)
		references state(id),
	constraint fk_candidate_state_id
		foreign key (race_id)
		references race(id),
	constraint fk_candidate_county_id
		foreign key (county_running)
		references county(id)
);

create table address (
	id int primary key auto_increment,
    streetName varChar(50) not null,
    townName varChar(35) not null,
    zipCode int not null,
    county_id int not null,
	constraint fk_address_county_id
		foreign key (county_id)
		references county(id)
);

create table person (
	id int primary key auto_increment,
	firstName varchar(50) not null,
	lastName varchar(50) not null,
	email varchar(75) not null,
    password varchar(50) not null,
	age int not null,
    address_id int not null,
    is_admin bool not null default false,
    poll_station int not null default 1,
	constraint fk_person_address_id
		foreign key (address_id)
		references address(id),
	constraint fk_person_race_id
		foreign key (poll_station)
		references race(id)
);

CREATE TABLE ElectionResults (
  id INT AUTO_INCREMENT PRIMARY KEY,
  participants INT NOT NULL,
  votes INT NOT NULL,
  outcome VARCHAR(255) NOT NULL,
  date DATE NOT NULL
);

-- DROP TABLE IF EXISTS currentElectionResults;

CREATE TABLE currentElectionResults (
    result_id INT AUTO_INCREMENT PRIMARY KEY,
    election_date DATE NOT NULL,
    state_id INT NOT NULL,
    candidate_id INT NOT NULL,
    votes_received INT NOT NULL,
    percentage_votes DECIMAL(5,2),
    total_votes INT NOT NULL,
    electoral_votes INT,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (state_id) REFERENCES state(id),
    FOREIGN KEY (candidate_id) REFERENCES candidate(id)
);

create table vote (
	id int primary key auto_increment,
    user_id int not null,
    race_id int not null, 
    candidate_id int not null,
    constraint fk_vote_person_id
		foreign key (user_id)
		references person(id),
	constraint fk_vote_race_id
		foreign key (race_id)
		references race(id),
	constraint fk_vote_candidate_id
		foreign key (candidate_id)
		references candidate(id)
);

create table user_requests (
    id int primary key auto_increment,
    user_id int not null, -- foreign key to the 'users' table
    document_type_1 varchar(255) not null,
    document_type_2 varchar(255) not null,
    request_status enum('pending', 'approved', 'denied') default 'pending',
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp,
    foreign key (user_id) references person(id)
);

