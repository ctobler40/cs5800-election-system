INSERT INTO vote (user_id, race_id, candidate_id)
SELECT 
    p.id AS user_id,
    r.id AS race_id,
    1 AS candidate_id -- Set to 1 to indicate no vote cast
FROM 
    person p
CROSS JOIN 
    (SELECT id FROM race WHERE id > 1) as r; -- Select races, skipping the first (race count - 1)

-- Update candidate_id in vote table based on raceType
UPDATE vote v
JOIN race r ON v.race_id = r.id
SET v.candidate_id = CASE 
    WHEN r.raceType = 1 THEN 
        CASE 
            WHEN RAND() < 0.6 THEN 2 -- 60% chance for candidate_id = 2
            WHEN RAND() < 0.8 THEN 3 -- 20% chance for candidate_id = 3
            ELSE FLOOR(4 + (RAND() * 6)) -- Remaining chance for candidate_id between 4 and 9
        END
    WHEN r.raceType = 2 THEN FLOOR(10 + (RAND() * 51)) -- Random value between 10 and 60
    WHEN r.raceType = 3 THEN FLOOR(65 + (RAND() * 51)) -- Random value between 65 and 115
END;

